// Compilation Process
// -------------------
    // - Preprocessing : Handles directives like #include and #define, before actual compilation starts.
    // - Compiling     : Converts C++ source code (.cpp file) into an intermediate object file (.o or .obj).
    // - Linking       : Combines object files and libraries into an executable program.




// Steps to Compile and Execute
// ----------------------------
    // - Write the Code: Create a C++ source code file (e.g., program.cpp).
    // - Compile: Use a C++ compiler (e.g., g++, clang) to compile the program.
    // - Linking: The compiler links the object files with necessary libraries to generate the executable.
    // - Run the Program: Execute the generated executable file.
    // - Example
        // g++ program.cpp -o program
        // program.exe




// Common Compilation Errors
// -------------------------
    // - Syntax Errors: Missing semicolons, incorrect syntax, etc.
    // - Linker Errors: Undefined functions or missing libraries.




